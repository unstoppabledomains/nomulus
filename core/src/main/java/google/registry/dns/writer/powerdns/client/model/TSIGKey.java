package google.registry.dns.writer.powerdns.client.model;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class TSIGKey {
  @JsonProperty("name")
  private String name;

  @JsonProperty("id")
  private String id;

  @JsonProperty("algorithm")
  private String algorithm;

  @JsonProperty("key")
  private String key;

  @JsonProperty("type")
  private String type;

  public static TSIGKey createTSIGKey(String name, String algorithm) {
    TSIGKey tsigKey = new TSIGKey();
    tsigKey.setName(name);
    tsigKey.setAlgorithm(algorithm);
    return tsigKey;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getAlgorithm() {
    return algorithm;
  }

  public void setAlgorithm(String algorithm) {
    this.algorithm = algorithm;
  }

  public String getKey() {
    return key;
  }

  public void setKey(String key) {
    this.key = key;
  }

  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }

  @Override
  public String toString() {
    return String.format("{name:%s,id:%s,algorithm:%s,type:%s}", name, id, algorithm, type);
  }
}
